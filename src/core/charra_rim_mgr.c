CHARRA_RC charra_get_reference_pcrs_sha256(
	const uint8_t reference_pcr_selection[TPM2_MAX_PCRS],
	const uint32_t reference_pcr_selection_len,
	uint8_t* reference_pcrs[TPM2_MAX_PCRS]) {

	/* sanity check */
	if (reference_pcr_selection_len >= TPM2_MAX_PCRS) {
		charra_log_error(
			"Bad PCR selection length: %d.", reference_pcr_selection_len);
		return CHARRA_RC_BAD_ARGUMENT;
	}

	for (size_t i = 0; i < reference_pcr_selection_len; ++i) {
		uint16_t pcr = reference_pcr_selection[i];

		/* sanity check */
		if (pcr >= TPM2_MAX_PCRS) {
			charra_log_error("Bad PCR: %d.", pcr);
			return CHARRA_RC_BAD_ARGUMENT;
		}

		reference_pcrs[i] = (uint8_t*)&REFERENCE_PCRS_SHA256[i][0];
	}

	return CHARRA_RC_SUCCESS;
}
